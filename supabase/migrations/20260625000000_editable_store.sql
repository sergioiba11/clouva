create policy "public read store media" on storage.objects for select using (bucket_id in ('products','categories','banners'));
create policy "authenticated upload store media" on storage.objects for insert to authenticated with check (bucket_id in ('products','categories','banners'));
create policy "authenticated update store media" on storage.objects for update to authenticated using (bucket_id in ('products','categories','banners')) with check (bucket_id in ('products','categories','banners'));
create policy "authenticated delete store media" on storage.objects for delete to authenticated using (bucket_id in ('products','categories','banners'));
